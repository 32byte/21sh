#include "libft.h"

size_t	ft_stroccur(const char *s, char c)
{
	size_t i;

	if (!s || ft_isprint(c) == 0)
		return (0);
	i = 0;
	while (*s)
		if (*s++ == c)
			i++;
	return (i);
}
