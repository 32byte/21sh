/*
**  renvoie un pointeur vers le premier caractere 'c' trouv√© sinon renvoie NULL
*/

#include "ft_string.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != c && *s != '\0')
		s++;
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
